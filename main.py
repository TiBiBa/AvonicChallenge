    def menu(self):
        print("Select one of the following options:\n")
        print("(a) Select a Pokémon")
        # We can only start a fight if we have already selected a Pokémon
        if self.game.pokemon_selected():
            print("(s) Start a battle")
        print("(d) List all Pokémon")
        print("(q) Quit")

        choice = input("\nWhat would you like to do? ")
        if choice not in self.valid_options:
            print("You made an invalid choice, try again!\n")
            return self.menu()
        if choice == "a":
            self.game.select_pokemon()
        elif choice == "s" and self.game.pokemon_selected():
            self.game.start_battle()
        elif choice == "d":
            self.game.list_pokemons()
        else:
            print("Thanks for playing!")
            sys.exit()
        self.menu()


